#include "libft.h"

char	*ft_substr(char const *src, unsigned int start, size_t len)
{
	char	*sub;
	size_t	s_len;

	if (!src)
		return (NULL);
	s_len = ft_strlen(src);
	if (start >= s_len || len == 0)
		return (ft_strdup(""));
	if (len > s_len - start)
		len = s_len - start;
	sub = (char *)malloc(sizeof(char) * (len + 1));
	if (!sub)
		return (NULL);
	ft_strlcpy(sub, src + start, len + 1);
	return (sub);
}
